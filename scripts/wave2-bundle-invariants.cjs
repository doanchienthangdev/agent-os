#!/usr/bin/env node
/**
 * scripts/wave2-bundle-invariants.cjs
 *
 * Reads knowledge/cross-tier-invariants.yaml and emits a TS module consumed by
 * the consistency-sweep skill (Bài #20 / Cross-Tier Consistency Engine).
 *
 * Output: supabase/functions/_shared/invariants.generated.ts
 *
 * Run: pnpm wave2:bundle-invariants
 *
 * Why this exists:
 *   Edge Functions run on Deno without filesystem access to the repo at request
 *   time. v1.0b hand-maintained the L3_INVARIANTS array in invariants.ts (a
 *   known DRY violation); v1.0c generates it from the yaml so a single source
 *   of truth drives all 3 detection layers (L1 cjs validators, L2 cjs
 *   validators, L3 sweep skill).
 *
 * Validation:
 *   - cross-tier-invariants.yaml must already pass `pnpm validate` (schema).
 *   - This script enforces 2 additional structural invariants:
 *       1. every entry has a non-empty `id`
 *       2. ids are unique
 *
 * Module API:
 *   exports.bundle({ yamlPath?, outPath?, now? })
 *   Returns { count, layer_counts: {L1, L2, L3}, ids, outPath }.
 */

'use strict';

const fs = require('fs');
const path = require('path');

let yaml;
try {
  yaml = require('js-yaml');
} catch (e) {
  console.error('❌ Missing dependency: js-yaml. Run: pnpm install');
  process.exit(2);
}

const REPO_ROOT = path.resolve(__dirname, '..');
const DEFAULT_YAML = path.join(REPO_ROOT, 'knowledge', 'cross-tier-invariants.yaml');
const DEFAULT_OUT = path.join(
  REPO_ROOT,
  'supabase',
  'functions',
  '_shared',
  'invariants.generated.ts',
);

function bundle({ yamlPath = DEFAULT_YAML, outPath = DEFAULT_OUT, now = () => new Date().toISOString() } = {}) {
  if (!fs.existsSync(yamlPath)) {
    throw new Error(`source yaml not found: ${yamlPath}`);
  }
  const raw = fs.readFileSync(yamlPath, 'utf8');
  let parsed;
  try {
    parsed = yaml.load(raw);
  } catch (e) {
    throw new Error(`yaml parse error: ${e.message}`);
  }
  if (!parsed || typeof parsed !== 'object') {
    throw new Error('yaml parsed to non-object');
  }
  const invariants = parsed.invariants || [];
  if (!Array.isArray(invariants)) {
    throw new Error('`invariants` must be an array');
  }

  const seenIds = new Set();
  for (const inv of invariants) {
    if (!inv || !inv.id || typeof inv.id !== 'string') {
      throw new Error(`invariant missing string id: ${JSON.stringify(inv)}`);
    }
    if (seenIds.has(inv.id)) {
      throw new Error(`duplicate invariant id: ${inv.id}`);
    }
    seenIds.add(inv.id);
  }

  const layerCounts = { L1: 0, L2: 0, L3: 0, unknown: 0 };
  for (const inv of invariants) {
    const layer = inv.layer || 'unknown';
    if (layerCounts[layer] === undefined) layerCounts[layer] = 0;
    layerCounts[layer] += 1;
  }

  const sourceVersion = parsed.version || '0.0.0';
  const ts = now();
  const banner =
    '// AUTO-GENERATED by scripts/wave2-bundle-invariants.cjs from\n' +
    '// knowledge/cross-tier-invariants.yaml. Do not edit by hand.\n' +
    '// Re-run with: pnpm wave2:bundle-invariants\n' +
    '//\n' +
    `// Source version: ${sourceVersion}\n` +
    `// Invariant count: ${invariants.length}\n` +
    `// By layer: L1=${layerCounts.L1} L2=${layerCounts.L2} L3=${layerCounts.L3}` +
    (layerCounts.unknown ? ` unknown=${layerCounts.unknown}` : '') +
    '\n' +
    `// Generated at: ${ts}\n\n`;

  // Emit just the data — the types and helpers live in invariants.ts.
  // Filtering convention:
  //   ALL_INVARIANTS = every entry in the yaml (live + deferred)
  //   *_LIVE        = layer filter AND status=='live' (default when status omitted)
  // Sweep + executor use *_LIVE; admin tools may inspect ALL.
  const body =
    'import type { Invariant } from "./invariants.ts";\n\n' +
    'export const ALL_INVARIANTS: Invariant[] = ' +
    JSON.stringify(invariants, null, 2) +
    ' as unknown as Invariant[];\n\n' +
    'function isLive(i: Invariant): boolean {\n' +
    '  return (i.status ?? "live") === "live";\n' +
    '}\n\n' +
    'export const L1_INVARIANTS_LIVE: Invariant[] = ALL_INVARIANTS.filter((i) => i.layer === "L1" && isLive(i));\n' +
    'export const L2_INVARIANTS_LIVE: Invariant[] = ALL_INVARIANTS.filter((i) => i.layer === "L2" && isLive(i));\n' +
    'export const L3_INVARIANTS_LIVE: Invariant[] = ALL_INVARIANTS.filter((i) => i.layer === "L3" && isLive(i));\n';

  const outDir = path.dirname(outPath);
  if (!fs.existsSync(outDir)) {
    fs.mkdirSync(outDir, { recursive: true });
  }
  fs.writeFileSync(outPath, banner + body, 'utf8');

  return {
    count: invariants.length,
    layer_counts: layerCounts,
    ids: invariants.map((i) => i.id),
    outPath,
  };
}

if (require.main === module) {
  try {
    const result = bundle();
    console.log(`✓ Bundled ${result.count} invariant(s) to ${path.relative(REPO_ROOT, result.outPath)}`);
    console.log(
      `  By layer: L1=${result.layer_counts.L1} L2=${result.layer_counts.L2} L3=${result.layer_counts.L3}` +
        (result.layer_counts.unknown ? ` unknown=${result.layer_counts.unknown}` : ''),
    );
    for (const id of result.ids) console.log(`    - ${id}`);
  } catch (e) {
    console.error(`❌ Bundle failed: ${e.message}`);
    process.exit(1);
  }
}

module.exports = { bundle };
