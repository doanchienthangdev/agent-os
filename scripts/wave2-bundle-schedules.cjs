#!/usr/bin/env node
/**
 * scripts/wave2-bundle-schedules.cjs
 *
 * Reads knowledge/schedules.yaml and emits a TS module consumed by the
 * scheduled-run-dispatcher Edge Function (Bài #8).
 *
 * Output: supabase/functions/_shared/schedules.generated.ts
 *
 * Run: pnpm wave2:bundle-schedules
 *
 * Why this exists:
 *   The Edge Function runs on Deno and cannot read knowledge/schedules.yaml
 *   at request time (no filesystem access in production). Bundling at build
 *   time produces a TS module the dispatcher imports. Until this script ran,
 *   the dispatcher returned `unknown_schedule` for every id.
 *
 * Validation:
 *   - schedules.yaml must already pass `pnpm validate` (Tier 1 schema check).
 *     This script does NOT re-run schema validation; it only enforces the
 *     two structural invariants the dispatcher cares about:
 *       1. every entry has a non-empty `id`
 *       2. ids are unique
 *
 * Module API:
 *   exports.bundle({ yamlPath?, outPath?, now? })
 *     - yamlPath: defaults to <repo>/knowledge/schedules.yaml
 *     - outPath:  defaults to <repo>/supabase/functions/_shared/schedules.generated.ts
 *     - now:      override timestamp for deterministic tests
 *   Returns { count, ids, outPath }.
 */

const fs = require('fs');
const path = require('path');
const yaml = require('js-yaml');

const REPO_ROOT = path.resolve(__dirname, '..');
const DEFAULT_YAML = path.join(REPO_ROOT, 'knowledge', 'schedules.yaml');
const DEFAULT_OUT = path.join(
  REPO_ROOT,
  'supabase',
  'functions',
  '_shared',
  'schedules.generated.ts',
);

function bundle(opts) {
  const yamlPath = (opts && opts.yamlPath) || DEFAULT_YAML;
  const outPath = (opts && opts.outPath) || DEFAULT_OUT;
  const now = (opts && opts.now) || new Date().toISOString();

  if (!fs.existsSync(yamlPath)) {
    throw new Error(`schedules.yaml not found at ${yamlPath}`);
  }

  const raw = fs.readFileSync(yamlPath, 'utf8');
  let parsed;
  try {
    parsed = yaml.load(raw);
  } catch (e) {
    throw new Error(`yaml parse failed: ${e.message}`);
  }

  if (!parsed || typeof parsed !== 'object') {
    throw new Error('schedules.yaml: top level must be an object');
  }
  if (!Array.isArray(parsed.schedules)) {
    throw new Error('schedules.yaml: missing `schedules` array');
  }

  const schedules = {};
  for (const entry of parsed.schedules) {
    if (!entry || typeof entry !== 'object') {
      throw new Error(`schedules.yaml: entry is not an object: ${JSON.stringify(entry)}`);
    }
    if (typeof entry.id !== 'string' || entry.id.length === 0) {
      throw new Error(`schedules.yaml: entry missing string id: ${JSON.stringify(entry)}`);
    }
    if (Object.prototype.hasOwnProperty.call(schedules, entry.id)) {
      throw new Error(`schedules.yaml: duplicate id "${entry.id}"`);
    }
    schedules[entry.id] = entry;
  }

  const ids = Object.keys(schedules).sort();

  const banner =
    `// AUTO-GENERATED by scripts/wave2-bundle-schedules.cjs from knowledge/schedules.yaml.\n` +
    `// Do not edit by hand. Re-run with: pnpm wave2:bundle-schedules\n` +
    `//\n` +
    `// Source version:  ${parsed.version || 'unknown'}\n` +
    `// Source timezone: ${parsed.timezone || 'unknown'}\n` +
    `// Schedule count:  ${ids.length}\n` +
    `// Generated at:    ${now}\n` +
    `\n`;

  const body =
    `import type { ScheduleEntry } from "./dispatcher.ts";\n` +
    `\n` +
    `export const SCHEDULES: Record<string, ScheduleEntry> = ${JSON.stringify(schedules, null, 2)};\n` +
    `\n` +
    `export const SCHEDULE_IDS: readonly string[] = ${JSON.stringify(ids)} as const;\n`;

  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, banner + body);

  return { count: ids.length, ids, outPath };
}

if (require.main === module) {
  try {
    const result = bundle();
    const rel = path.relative(REPO_ROOT, result.outPath);
    console.log(`✓ Bundled ${result.count} schedule(s) to ${rel}`);
    for (const id of result.ids) console.log(`    - ${id}`);
    process.exit(0);
  } catch (e) {
    console.error(`✗ ${e.message}`);
    process.exit(1);
  }
}

module.exports = { bundle, DEFAULT_YAML, DEFAULT_OUT };
