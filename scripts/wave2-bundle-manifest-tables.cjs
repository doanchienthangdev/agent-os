#!/usr/bin/env node
/**
 * scripts/wave2-bundle-manifest-tables.cjs
 *
 * Reads knowledge/manifest.yaml and emits a TS module exposing the list of
 * tables declared under tier2_operational. Consumed by the L3 sweep skill
 * (consistency-sweep) so the executor compares live DB state against a list
 * derived directly from manifest.yaml — not a hand-maintained constant that
 * drifts.
 *
 * Output: supabase/functions/_shared/manifest-tables.generated.ts
 *
 * Run: pnpm wave2:bundle-manifest-tables
 *
 * Module API:
 *   exports.bundle({ yamlPath?, outPath?, now? })
 *   Returns { ops_count, metrics_count, ops_tables, metrics_tables, outPath }.
 */

'use strict';

const fs = require('fs');
const path = require('path');

let yaml;
try {
  yaml = require('js-yaml');
} catch (e) {
  console.error('❌ Missing dependency: js-yaml. Run: pnpm install');
  process.exit(2);
}

const REPO_ROOT = path.resolve(__dirname, '..');
const DEFAULT_YAML = path.join(REPO_ROOT, 'knowledge', 'manifest.yaml');
const DEFAULT_OUT = path.join(
  REPO_ROOT,
  'supabase',
  'functions',
  '_shared',
  'manifest-tables.generated.ts',
);

function extractTables(parsed, schemaName) {
  const schemas = parsed && parsed.tier2_operational && parsed.tier2_operational.schemas;
  if (!schemas || !schemas[schemaName]) return [];
  const tables = schemas[schemaName].tables;
  if (!Array.isArray(tables)) return [];
  return tables
    .filter((t) => !t.kind || t.kind !== 'view')
    .map((t) => t.name)
    .filter((n) => typeof n === 'string' && n.length > 0)
    .sort();
}

function bundle({ yamlPath = DEFAULT_YAML, outPath = DEFAULT_OUT, now = () => new Date().toISOString() } = {}) {
  if (!fs.existsSync(yamlPath)) {
    throw new Error(`source yaml not found: ${yamlPath}`);
  }
  const raw = fs.readFileSync(yamlPath, 'utf8');
  let parsed;
  try {
    parsed = yaml.load(raw);
  } catch (e) {
    throw new Error(`yaml parse error: ${e.message}`);
  }
  if (!parsed || typeof parsed !== 'object') {
    throw new Error('yaml parsed to non-object');
  }
  const opsTables = extractTables(parsed, 'ops');
  const metricsTables = extractTables(parsed, 'metrics');

  // Sanity: every name must be unique within its schema.
  for (const [name, list] of [['ops', opsTables], ['metrics', metricsTables]]) {
    const seen = new Set();
    for (const t of list) {
      if (seen.has(t)) throw new Error(`duplicate ${name}.${t} in manifest`);
      seen.add(t);
    }
  }

  const sourceVersion = parsed.version || '0.0.0';
  const ts = now();
  const banner =
    '// AUTO-GENERATED by scripts/wave2-bundle-manifest-tables.cjs from\n' +
    '// knowledge/manifest.yaml. Do not edit by hand.\n' +
    '// Re-run with: pnpm wave2:bundle-manifest-tables\n' +
    '//\n' +
    `// Source version: ${sourceVersion}\n` +
    `// ops tables:     ${opsTables.length}\n` +
    `// metrics tables: ${metricsTables.length}\n` +
    `// Generated at:   ${ts}\n\n`;

  const body =
    'export const MANIFEST_OPS_TABLES: readonly string[] = Object.freeze(\n' +
    JSON.stringify(opsTables, null, 2) +
    '\n);\n\n' +
    'export const MANIFEST_METRICS_TABLES: readonly string[] = Object.freeze(\n' +
    JSON.stringify(metricsTables, null, 2) +
    '\n);\n';

  const outDir = path.dirname(outPath);
  if (!fs.existsSync(outDir)) {
    fs.mkdirSync(outDir, { recursive: true });
  }
  fs.writeFileSync(outPath, banner + body, 'utf8');

  return {
    ops_count: opsTables.length,
    metrics_count: metricsTables.length,
    ops_tables: opsTables,
    metrics_tables: metricsTables,
    outPath,
  };
}

if (require.main === module) {
  try {
    const result = bundle();
    console.log(
      `✓ Bundled ${result.ops_count} ops + ${result.metrics_count} metrics table(s) to ${path.relative(REPO_ROOT, result.outPath)}`,
    );
  } catch (e) {
    console.error(`❌ Bundle failed: ${e.message}`);
    process.exit(1);
  }
}

module.exports = { bundle, extractTables };
